    /**
     * Pesquisa todos os habitats cadastrados no banco para que seja
     * populado do combobox, assim evita de criar valores sem estarem cadastrados
     * no banco de dados.
     */
    private void pesquisarHabitatsEMontarComboBox() {
        ManterHabitatDAO habitatDAO = new ManterHabitatDAO();

        try {
            for (Habitat h : habitatDAO.pesquisarTudo()) {
                jCBhabitat.addItem(h);
            }
        } catch (Exception e) {
            throw new RuntimeException(e.getMessage());
        }
    }

