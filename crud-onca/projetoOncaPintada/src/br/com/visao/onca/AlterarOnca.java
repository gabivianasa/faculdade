    private void jBpesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBpesquisarActionPerformed
        // TODO add your handling code here:
        onca.setIdOnca(Integer.parseInt(jTid.getText()));

        try {
            oncaDAO.pesquisarRegistro(onca);

            jTnome.setText(onca.getNome());
            jTidade.setText(Integer.toString(onca.getIdade()));
            jTpeso.setText(Double.toString(onca.getIdade()));
            jTcomportamento.setText(onca.getComportamento());
            jTdescricaoFisica.setText(onca.getDescricaoFisica());

            // setar sexo
            if (onca.getSexo().equals("F")) {
                jRfemea.setSelected(true);
            } else if (onca.getSexo().equals("M")) {
                jRmacho.setSelected(true);
            }

            // setar habitat
            Integer habitatOnca = onca.getIdHabitat();
            
            for (int i = 0; i < jCBhabitat.getItemCount(); i++) {
                Habitat h = jCBhabitat.getItemAt(i);

                if (h.getIdHabitat() == habitatOnca) {
                    jCBhabitat.setSelectedIndex(i); // deixa o habitat selecionado na combobox
                    break; // para o for, assim evita problemas, pois não faz sentido continuar percorrendo
                }
            }

            // setar viva ou morta
            if (onca.getInViva() == 1) {
                jRviva.setSelected(true);
            } else {
                jRobito.setSelected(true);
            }

            jBpesquisar.setEnabled(false);
            this.jTid.setEnabled(false);

        } catch (Exception e) {
            Logger.getLogger(AlterarOnca.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_jBpesquisarActionPerformed

    private void jBeditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBeditarActionPerformed
        // TODO add your handling code here:

        if (this.verificarCamposEmBranco()) {
            JOptionPane.showMessageDialog(null, "Verifique o(s) campo(s) não preenchido(s)!");
            return;
        }

        onca.setNome(jTnome.getText());
        onca.setSexo(this.pegarSexoOncaSelecionada());
        onca.setIdade(Integer.parseInt(jTidade.getText()));
        onca.setPeso(Double.parseDouble(jTpeso.getText()));
        onca.setComportamento(jTcomportamento.getText());
        onca.setDescricaoFisica(jTdescricaoFisica.getText());
        onca.setDataUltimaObservacao(new Timestamp(System.currentTimeMillis()));
        onca.setInViva(this.pegarStatusVidaOncaSelecionado());

        Habitat h = (Habitat) jCBhabitat.getSelectedItem();
        onca.setIdHabitat(h.getIdHabitat());

        try {
            oncaDAO.editarOnca(onca);

            this.jTnome.setText("");
            this.jRfemea.setSelected(false);
            this.jRmacho.setSelected(false);
            this.jTidade.setText("");
            this.jTpeso.setText("");
            this.jTcomportamento.setText("");
            this.jTdescricaoFisica.setText("");
            this.jRviva.setSelected(false);
            this.jRobito.setSelected(false);
            this.jCBhabitat.setSelectedIndex(-1);
            this.jBpesquisar.setEnabled(true);
            this.jTid.setText("");
            JOptionPane.showMessageDialog(null, "Cadastro efetuado com sucesso!");
        } catch (Exception e) {
            Logger.getLogger(AlterarOnca.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_jBeditarActionPerformed

    private byte pegarStatusVidaOncaSelecionado() {
        if (jRviva.isSelected()) {
            return 1;
        } else {
            return 0;
        }
    }

    private String pegarSexoOncaSelecionada() {
        if (jRfemea.isSelected()) {
            return "F";
        } else {
            return "M";
        }
    }

    private boolean verificarCamposEmBranco() {
        boolean nenhumSexoSelecionado = !jRfemea.isSelected() && !jRmacho.isSelected();
        boolean nenhumStatusVidaSelecionado = !jRviva.isSelected() && !jRobito.isSelected();

        return jTnome.getText().isEmpty()
                || nenhumSexoSelecionado
                || nenhumStatusVidaSelecionado
                || jTidade.getText().isEmpty()
                || jTpeso.getText().isEmpty()
                || jTcomportamento.getText().isEmpty()
                || jTdescricaoFisica.getText().isEmpty()
                || jCBhabitat.getSelectedItem() == null;

    }

