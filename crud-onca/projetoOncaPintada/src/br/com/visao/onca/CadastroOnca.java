    private void jBcadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBcadastrarActionPerformed
        // TODO add your handling code here:
        Onca onca = new Onca();

        if (this.verificarCamposEmBranco()) {
            JOptionPane.showMessageDialog(null, "Verifique o(s) campo(s) não preenchido(s)!");
            return;
        }

        onca.setNome(jTnome.getText());
        onca.setSexo(this.pegarSexoOncaSelecionado());
        onca.setIdade(Integer.parseInt(jTidade.getText()));
        onca.setPeso(Double.parseDouble(jTpeso.getText()));
        onca.setComportamento(jTcomportamento.getText());
        onca.setDescricaoFisica(jTdescricaoFisica.getText());

        Habitat h = (Habitat) jCBhabitat.getSelectedItem();
        onca.setIdHabitat(h.getIdHabitat());

        try {
            ManterOncaDAO oncaDAO = new ManterOncaDAO();
            oncaDAO.inserir(onca);

            this.jTcomportamento.setText("");
            this.jTdescricaoFisica.setText("");
            this.jTidade.setText("");
            this.jTnome.setText("");
            this.jTpeso.setText("");
            this.jRfemea.setSelected(false);
            this.jRmacho.setSelected(false);
            this.jCBhabitat.setSelectedIndex(-1);

            JOptionPane.showMessageDialog(null, "Cadastro efetuado com sucesso!");
        } catch (Exception e) {
            System.getLogger(CadastroOnca.class.getName()).log(System.Logger.Level.ERROR, (String) null, e);
        }
    }//GEN-LAST:event_jBcadastrarActionPerformed

    private String pegarSexoOncaSelecionado() {
        if (jRfemea.isSelected()) {
            return "F";
        } else {
            return "M";
        }
    }

