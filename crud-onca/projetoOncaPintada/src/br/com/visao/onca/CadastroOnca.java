    private boolean verificarCamposEmBranco() {
        boolean nenhumSexoSelecionado = !jRfemea.isSelected() && !jRmacho.isSelected();

        return jTnome.getText().isEmpty()
                || nenhumSexoSelecionado
                || jTidade.getText().isEmpty()
                || jTpeso.getText().isEmpty()
                || jTcomportamento.getText().isEmpty()
                || jTdescricaoFisica.getText().isEmpty()
                || jCBhabitat.getSelectedItem() == null;

    }

