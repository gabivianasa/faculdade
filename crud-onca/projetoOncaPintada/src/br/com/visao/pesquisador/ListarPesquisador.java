    public ListarPesquisador() {
        initComponents();

        Pesquisador pesquisador = new Pesquisador();
        ManterPesquisadorDAO pesquisadorDAO = new ManterPesquisadorDAO();
        DefaultTableModel tabelaPesquisador = (DefaultTableModel) jTpesquisador.getModel();
        tabelaPesquisador.setRowCount(0);
        ArrayList<Pesquisador> pesquisadores = null;
        try {
            pesquisadores = pesquisadorDAO.pesquisarTudo();
        } catch (Exception ex) {
            Logger.getLogger(ListarHabitat.class.getName()).log(Level.SEVERE, null, ex);
        }
        for (int i = 0; i < pesquisadores.size(); i++) {
            pesquisador = pesquisadores.get(i);
            tabelaPesquisador.addRow(new Object[]{
                pesquisador.getIdPesquisador(),
                pesquisador.getNome(),
                pesquisador.getEmail(),
                pesquisador.getCpf(),
                pesquisador.getEndereco()
            });
        }
    }

